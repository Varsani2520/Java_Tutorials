public class AssignmentOperator {
    public static void main(Strings[] args) {
        int a = 10; // define var
        
        a += 2; // increment by 2
        System.out.println(a);

        a -= 2; // decrement by 2
        System.out.println(a);

        a++; // increment by 1
        System.out.println(a);

        a--; // increment by 1

        System.out.println(a);
        ++a; //first inc after read a

        System.out.println(a);
        --a; // first dec after a read

        System.out.println(a);
    }
}

