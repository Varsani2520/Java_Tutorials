public class Variable {
    public static void main(Strings[] args) {
        // integer
        int num=10;
        System.out.println("integer variable:" + num);

        // float
        float number=10;
        System.out.println("floating variable:" + number);

        // character
        char a='A';
        System.out.println("character variable:" + a);
        
        // boolean
        boolean name=true;
        System.out.println("boolean variable:" + name);
       
    }
}

